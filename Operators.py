# Basic arithmetic operations
a = 10
b = 3

print(a + b)   # 13
print(a - b)   # 7
print(a * b)   # 30
print(a / b)   # 3.333...
print(a // b)  # 3
print(a % b)   # 1
print(a ** b)  # 1000




# Basic comparison operations
print(a == b)  
print(a != b)       
print(a > b)   
print(a < b)   
print(a >= b)  
print(a <= b)  


# Basic logical operations
x = True
y = False
print(x and y)
print(x or y)
print(not x)
print(not y)
print(x and not y)
print((a > b) and (b < 5))
print((a < b) or (b < 5))
print(not (a == b))
print((a + b) > (a - b))
print((a * b) == (a // b) * b + (a % b))









